public class Move {
   int rowMove;
   int colMove;

   public Move(int rowMove, int colMove) {
       this.rowMove = rowMove;
       this.colMove = colMove;
   }

   public int getRowMove() {
       return rowMove;
   }

   public void setRowMove(int rowMove) {
       this.rowMove = rowMove;
   }

   public int getColMove() {
       return colMove;
   }

   public void setColMove(int colMove) {
       this.colMove = colMove;
   }
}

